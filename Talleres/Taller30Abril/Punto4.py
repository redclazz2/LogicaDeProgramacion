num1 = float(input("Ingrese el primer número real: "))
num2 = float(input("Ingrese el segundo número real: "))

sum = num1 + num2
res = num1 - num2
mul = num1 * num2
div = num1 / num2
resto = num1 % num2

print("El resultado de la suma es:",sum)
print("El resultado de la resta es:",res)
print("El resultado de la multiplicación es:",mul)
print("El resultado de la división es:",div)
print("El resultado del resto es:",resto)